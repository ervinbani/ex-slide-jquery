$(document).ready(function(){

    $("#hide").click(function(){
        $("#p1").hide();
        $("#hide").hide();
        $("#show").show();

    });
    $("#show").click(function(){
        $("#p1").show();
        $("#hide").show();
        $("#show").hide();
    });
    $("#hide2").click(function(){
        $("#p2").hide();
        $("#hide2").hide();
        $("#show2").show();

    });
    $("#show2").click(function(){
        $("#p2").show();
        $("#hide2").show();
        $("#show2").hide();
    });





});
